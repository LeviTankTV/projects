    // Override playerTurn method to handle player interaction with the magic tree
    @Override
    public void playerTurn(Game game, Room room) {
        Player player = game.getPlayer();
        System.out.println(Tree);

        describeTree();

        List<MagicalFruit> foundFruits = new ArrayList<>();
        Random random = new Random();

        int chance = random.nextInt(100);

        if (chance < 10) {
            for (int i = 0; i < 3; i++) {
                foundFruits.add(fruitFactory.createRandomFruit());
            }
        } else if (chance < 50) {
            for (int i = 0; i < 2; i++) {
                foundFruits.add(fruitFactory.createRandomFruit());
            }
        } else {
            foundFruits.add(fruitFactory.createRandomFruit());
        }

        if (foundFruits.isEmpty()) {
            System.out.println("К сожалению, вы ничего не нашли под магическим деревом.");
        } else {
            System.out.println("Вы нашли следующие магические фрукты:");
            for (MagicalFruit fruit : foundFruits) {
                System.out.println("- " + fruit.getClass().getSimpleName());
                player.getInventory().addItem(fruit);
            }
            System.out.println("Фрукты добавлены в ваш инвентарь.");
        }

        handleEmptyRoom(game, this);
    }

